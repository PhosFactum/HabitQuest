def get_exercise_by_muscle_group(group):
    exercises = {
        "ноги": ["Приседания", "Выпады", "Подъем на носки"],
        "спина": ["Тяга в наклоне", "Планка", "Разведение рук в наклоне"],
        "грудь": ["Отжимания", "Жим лёжа", "Разведение рук лёжа"],
        "пресс": ["Скручивания", "Подъём ног", "Планка"]
    }
    return exercises.get(group.lower(), ["Упражнения не найдены"])
