import random


def generate_workout(level="—Å—Ä–µ–¥–Ω—è—è"):
    base = {
        "–ª—ë–≥–∫–∞—è": ["–ü—Ä–∏—Å–µ–¥–∞–Ω–∏—è x10", "–ü–ª–∞–Ω–∫–∞ 30 —Å–µ–∫", "–ü—Ä—ã–∂–∫–∏ x10"],
        "—Å—Ä–µ–¥–Ω—è—è": ["–ü—Ä–∏—Å–µ–¥–∞–Ω–∏—è x20", "–ü–ª–∞–Ω–∫–∞ 60 —Å–µ–∫", "–ü—Ä—ã–∂–∫–∏ x20"],
        "—Ç—è–∂—ë–ª–∞—è": ["–ë—ë—Ä–ø–∏ x15", "–ü–ª–∞–Ω–∫–∞ 90 —Å–µ–∫", "–ü—Ä–∏—Å–µ–¥–∞–Ω–∏—è —Å –ø—Ä—ã–∂–∫–æ–º x15"]
    }

    exercises = base.get(level, base["—Å—Ä–µ–¥–Ω—è—è"])
    random.shuffle(exercises)  # –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è

    workout_text = "\n".join(exercises)  # –û–±—ä–µ–¥–∏–Ω—è–µ–º —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è
    workout_text += "\n\nüîÅ –ü–æ–≤—Ç–æ—Ä–∏—Ç—å –≤—Å—ë 4 —Ä–∞–∑–∞"  # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥—Ö–æ–¥—ã

    return workout_text